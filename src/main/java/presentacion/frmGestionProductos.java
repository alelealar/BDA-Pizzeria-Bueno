    private void cargarProductos(){
        try{
            IPizzaBO pizzaBO = new PizzaBO();
            List<PizzaDTO> pizzas = pizzaBO.obtenerProductos();
            panelProductos.removeAll();
            panelProductos.setLayout(new java.awt.GridLayout(0, 1, 10, 10)); 
            
            ArrayList<PizzaDTO> pizzasUnicas = pizzaBO.agruparPizzasPorNombre();
            
            for(PizzaDTO p: pizzasUnicas){
                String primerTamanio = p.getTamanios().get(0);
                double precio = p.getPrecios().get(0);
                int id = p.getIdsPorTamanio().get(primerTamanio);

                PizzaProductoDTO pizzaProd = new PizzaProductoDTO(
                    id,
                    p.getNombre(),
                    p.getDescripcion(),
                    p.getRutaImagen(),
                    primerTamanio,
                    precio,
                    p.getEstado()
                );

                panProductosTarjetas tarjeta = new panProductosTarjetas(pizzaProd, pizzaBO);
                panelProductos.add(tarjeta);
            }
            panelProductos.revalidate();
            panelProductos.repaint();
        } catch (NegocioException ex) {
            JOptionPane.showMessageDialog(this, "Error al cargar productos: " + ex.getMessage());
        }
    }
    
    
