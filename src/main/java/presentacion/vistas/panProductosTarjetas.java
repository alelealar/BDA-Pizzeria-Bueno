    private void btnActualizarPrecioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarPrecioActionPerformed
        if(!txtPrecioAct.getText().trim().matches("\\d+(\\.\\d{1,2})?")){
            JOptionPane.showMessageDialog(this, "El precio debe contener solo numeros");
            return;
        }
        
        try{
            double nuevoPrecio = Double.parseDouble(txtPrecioAct.getText());
            
            pizzaBO.actualizarPrecioPizza(pizza.getNombre(), pizza.getTamanio(), nuevoPrecio);
            
            pizza.setPrecio(nuevoPrecio);
            lblPrecioPizza.setText("$" + nuevoPrecio);
            
            JOptionPane.showMessageDialog(this, "Precio actualizado correctamente");
        } catch (NegocioException ex) {
            JOptionPane.showMessageDialog(this, "Error al actualizar el precio de la pizza");
        }
    }//GEN-LAST:event_btnActualizarPrecioActionPerformed

