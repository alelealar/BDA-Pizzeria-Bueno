    private void btnChicaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnChicaMouseClicked
        try{
            PizzaProductoDTO p = pizzaBO.obtenerPizzaPorTamano(pizza.getNombre(), "CHICA");
            
            lblEstadoPizza.setText(p.getEstado().toString());
            lblPrecioPizza.setText("$"+p.getPrecio());
            
            if (p.getRutaImagen() != null && !p.getRutaImagen().isEmpty()) {
                String rutaCorregida = p.getRutaImagen().replace("\\", "/");
                imgPizza.setIcon(new ImageIcon(rutaCorregida));
                imgPizza.setText("");
            }
            
            pizza = p;
        } catch (NegocioException ex) {
            Logger.getLogger(panProductosTarjetas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnChicaMouseClicked

